    // Создание сущности Publication
    public static Publication GetPublication(int publicationId) => new() 
    {
        Id = publicationId,
        Name = GetRandomTitle(),
        Price = Utils.GetRandom(100, 10_000),
        Type = GetRandomType() 
    };

    // Создание списка сущностей Section
    public static List<Publication> GetPublications(int count, Func<int, Publication> getPublication) => Enumerable
        .Range(1, count) 
        .Select(getPublication)
        .ToList();

    // Выбор "случайного" названия 
    private static string GetRandomTitle() => 
        Titles[Utils.GetRandom(0, Titles.Length - 1)];

    // Генератор случайной роли PublicationType
    private static PublicationType GetRandomType() =>
        Utils.GetRandom(1, 3) switch
        {
            1 => PublicationType.Newspaper,
            2 => PublicationType.Book,
            _ => PublicationType.Journal
        };

